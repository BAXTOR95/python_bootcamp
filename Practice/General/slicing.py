piano_keys = ["a", "b", "c", "d", "e", "f", "g"]

print(piano_keys)
print("[2:5] ->", piano_keys[2:5])
print("[2:] ->", piano_keys[2:])
print("[:5] ->", piano_keys[:5])
print("[2:5:2] ->", piano_keys[2:5:2])
print("[::2] ->", piano_keys[::2])
print("[::-1] ->", piano_keys[::-1])
print("[:-1] ->", piano_keys[:-1])
print("[-1] ->", piano_keys[-1])